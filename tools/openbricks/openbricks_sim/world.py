"""
World loading + chassis injection.

MuJoCo supports ``<include file="..."/>`` but the includable file
must itself be a complete MJCF. Our chassis is a *fragment* (a
``<body>``, an ``<actuator>``, and a ``<sensor>`` section with no
outer ``<mujoco>`` envelope) so that one generator can target many
worlds. To splice the fragment into a world MJCF we do a small
amount of textual surgery:

* The chassis's ``<worldbody>`` body goes just before the world's
  closing ``</worldbody>``.
* The chassis's ``<actuator>`` section lands just before the
  world's closing ``</mujoco>`` (creating one if the world didn't
  have any actuators).
* Ditto for ``<sensor>``.

That's simpler than programmatic MJCF manipulation with a DOM, and
fragile only in the sense that a world MJCF with very creative
formatting might break the substitutions. All shipped worlds follow
the same shape, so this works.
"""

from pathlib import Path
import re

import mujoco

from openbricks_sim.chassis import ChassisSpec, chassis_mjcf
from openbricks_sim import lego_mjcf


class WorldLoadError(Exception):
    pass


# A ``<lego_prop name="..." ldr="..." pos="x y z" mass="..."/>`` element
# inside ``<worldbody>`` is openbricks's per-prop placeholder. The
# loader replaces each instance with the LDraw-derived MJCF body
# generated by :mod:`openbricks_sim.lego_mjcf`. Per-prop ``.ldr``
# files are the source of truth (visually estimated brick layouts
# from the WRO BI PDFs); the MJCF is regenerated each load so editing
# the ``.ldr`` doesn't need a separate build step. Attribute order
# in the ``<lego_prop>`` tag is fixed for the regex below; if you
# rearrange them, update the regex.
_LEGO_PROP_RE = re.compile(
    r'<lego_prop\s+name="(?P<name>[^"]+)"\s+'
    r'ldr="(?P<ldr>[^"]+)"\s+'
    r'pos="(?P<pos>[^"]+)"\s+'
    r'mass="(?P<mass>[^"]+)"'
    r'(?:\s+color="(?P<color>[^"]+)")?'   # optional LDraw colour override
    r'\s*/>',
    re.DOTALL)


# LDraw colour-code aliases the ``<lego_prop color="...">`` attribute
# accepts. Numeric strings ("0", "1", "14") are parsed as literal
# LDraw colour codes; keyword strings (``"red"``, ``"blue"``) map to
# the canonical LDraw codes via this table. Mirrors the keys in
# ``lego_mjcf.COLOR_NAME_BY_LDRAW_CODE`` so both sides stay in sync.
_COLOR_KEYWORD_TO_LDRAW = {
    "black":  0, "blue":   1, "green":  2, "red":    4,
    "yellow": 14, "white": 15, "gray":  7,
}


def _expand_lego_props(world_xml: str, world_dir: Path) -> str:
    """Replace every ``<lego_prop .../>`` placeholder in ``world_xml``
    with its expanded LDraw → MJCF body.

    The ``ldr`` attribute is resolved relative to the world's
    directory (so ``ldr="props/clef.ldr"`` reads
    ``<world_dir>/props/clef.ldr``). The body's ``pos`` is the world
    origin of the LDraw model's frame; ``mass`` is the prop's total
    kg, distributed evenly across brick bodies (visual-only stud
    cylinders carry no mass).

    Raises :class:`WorldLoadError` if a referenced ``.ldr`` is
    missing — that's almost always a typo, surfaced loudly rather
    than silently dropped.
    """
    def _expand(m):
        ldr_path = world_dir / m.group("ldr")
        if not ldr_path.is_file():
            raise WorldLoadError(
                "lego_prop {!r} references missing .ldr file {!r}"
                .format(m.group("name"), str(ldr_path)))
        ldr_text = ldr_path.read_text()
        pos = tuple(float(t) for t in m.group("pos").split())
        if len(pos) != 3:
            raise WorldLoadError(
                "lego_prop {!r} pos must be 3 floats; got {!r}"
                .format(m.group("name"), m.group("pos")))
        color_attr = m.group("color")
        color_override = None
        if color_attr is not None:
            if color_attr.isdigit():
                color_override = int(color_attr)
            elif color_attr in _COLOR_KEYWORD_TO_LDRAW:
                color_override = _COLOR_KEYWORD_TO_LDRAW[color_attr]
            else:
                raise WorldLoadError(
                    "lego_prop {!r} color={!r} unrecognised — use a "
                    "numeric LDraw code or one of {}".format(
                        m.group("name"), color_attr,
                        sorted(_COLOR_KEYWORD_TO_LDRAW.keys())))
        return lego_mjcf.emit_prop_body(
            m.group("name"), pos, ldr_text,
            total_mass_kg=float(m.group("mass")),
            color_override=color_override)
    return _LEGO_PROP_RE.sub(_expand, world_xml)


def _extract_fragment_sections(fragment: str):
    """Split the chassis fragment into (body, actuators, sensors)."""
    def _grab(tag):
        m = re.search(
            r"  <" + tag + r">.*?  </" + tag + r">\n",
            fragment, re.DOTALL)
        return m.group(0) if m else ""
    # The chassis body lives inside the fragment's ``<worldbody>``.
    body = ""
    m = re.search(r"  <worldbody>\n(.*?)  </worldbody>\n",
                  fragment, re.DOTALL)
    if m:
        body = m.group(1)
    return body, _grab("actuator"), _grab("sensor")


def _inject(world_xml: str, body: str, actuators: str, sensors: str) -> str:
    """Return a new MJCF string with the chassis fragment spliced in."""
    # World body insertion.
    if "</worldbody>" not in world_xml:
        raise WorldLoadError("world MJCF has no </worldbody> to splice into")
    merged = world_xml.replace("</worldbody>",
                               body + "\n  </worldbody>", 1)
    # Actuators — insert before </mujoco>.
    if "<actuator>" in merged:
        merged = merged.replace("<actuator>",
                                actuators.strip() + "\n  <actuator>", 1)
    else:
        merged = merged.replace("</mujoco>", actuators + "</mujoco>", 1)
    # Sensors.
    if "<sensor>" in merged:
        merged = merged.replace("<sensor>",
                                sensors.strip() + "\n  <sensor>", 1)
    else:
        merged = merged.replace("</mujoco>", sensors + "</mujoco>", 1)
    return merged


def load_world(world_path: str,
               chassis_spec: ChassisSpec = None,
               chassis_name: str = "chassis"):
    """Load a world MJCF from disk and splice in the default chassis.

    Returns a tuple ``(mujoco.MjModel, mujoco.MjData, merged_mjcf)``
    ready to step. The ``merged_mjcf`` string is returned mostly for
    test / debugging visibility — callers normally work with the
    MjModel + MjData only.
    """
    p = Path(world_path)
    if not p.is_file():
        raise WorldLoadError("world file not found: " + str(world_path))
    world_xml = p.read_text()

    # Expand any ``<lego_prop .../>`` placeholders into full MJCF
    # bodies before splicing in the chassis. Generation happens at
    # load time so editing a per-prop ``.ldr`` doesn't need a
    # separate build pass.
    world_xml = _expand_lego_props(world_xml, p.parent)

    fragment = chassis_mjcf(chassis_spec, name=chassis_name)
    body, actuators, sensors = _extract_fragment_sections(fragment)
    merged = _inject(world_xml, body, actuators, sensors)

    try:
        # ``from_xml_string`` resolves relative texture paths against
        # the CWD, not the world file. Chdir trick so ``mat.png``
        # refs inside the world resolve.
        import os
        cwd = os.getcwd()
        os.chdir(str(p.parent))
        try:
            model = mujoco.MjModel.from_xml_string(merged)
        finally:
            os.chdir(cwd)
    except Exception as e:
        raise WorldLoadError(
            "MuJoCo couldn't parse the merged MJCF: {}".format(e)) from e

    data = mujoco.MjData(model)
    return model, data, merged
